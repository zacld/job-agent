"""
agent/notify.py — Daily digest email via Gmail API or SMTP fallback.

Sends an HTML email to MY_EMAIL with:
- Table of today's found jobs (score, company, role, apply method)
- Attached cover letters for 🟡 jobs
- Attached screenshots for 🟠 queued jobs
- Link to the Google Sheet
"""

import logging
import mimetypes
import os
import pathlib
import smtplib
from datetime import date
from email import encoders
from email.mime.base import MIMEBase
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText

import config

logger = logging.getLogger(__name__)


